    private static String trim(String str) {
        //remove caracteres desnecessarios
        str = str.replaceAll("[/.{}()\\[\\]]", "");
        //remove inicio: "L=...."
        //str = str.substring(2);
        //remove ultima virgula
        str = str.substring(0, str.length() - 1);
        return str;
    }


